# Figure 2: log2FC violin + stripplots per dataset
# ---------------------------------------------------------------------------

def plot_log2fc_violins(
    df_all: pd.DataFrame,
    meta_df: pd.DataFrame,
    out_path: Path = None,
) -> Path:
    """
    Two-panel figure showing the distribution of log2FC (expt / ref) per dataset
    for expression-adjusted genes (top) and essential genes (bottom).

    Each dataset is one column. Within each column, a violin shows the KDE of
    log2FC values and a stripplot overlays individual gene points. Genes absent
    from the experiment (tpm_y = 0, log2fc = -inf) are clipped to LOG2FC_FLOOR
    and shown as downward triangles.

    Datasets are ordered by outcome (success → sim_failed → parca_failed) then
    by descending all-gene Pearson r² within each group, matching Figure 1.

    Parameters
    ----------
    df_all : Long-format DataFrame from _load_allgenes(), with dataset_id,
             outcome, and all gene_metadata columns including log2fc.
    meta_df : Meta-summary DataFrame (used only for dataset ordering).
    out_path : Save path. Defaults to analysis/figures/meta_log2fc_violins.png.

    Returns
    -------
    Path where the figure was saved.
    """
    if out_path is None:
        out_path = FIGURES_DIR / 'meta_log2fc_violins.png'

    meta_sorted = _sort_datasets(meta_df)
    dataset_order = meta_sorted['dataset_id'].tolist()
    outcome_map   = meta_sorted.set_index('dataset_id')['outcome'].to_dict()
    n_ds = len(dataset_order)
    x_pos = np.arange(n_ds)

    panels = [
        ('has_expression_adjustment', 'Expression-adjusted genes'),
        ('is_essential',              'Essential genes'),
    ]

    rng = np.random.default_rng(42)

    fig, axes = plt.subplots(2, 1, figsize=(14, 9), sharex=True)
    fig.subplots_adjust(hspace=0.12)

    for ax, (flag_col, title) in zip(axes, panels):
        sub = df_all[df_all[flag_col].fillna(False).astype(bool)].copy()

        # Clip ±inf: -inf → floor (absent from expt), +inf → NaN (absent from ref, rare)
        sub['_y'] = (
            sub['log2fc']
            .replace(-np.inf, LOG2FC_FLOOR)
            .replace(np.inf, np.nan)
        )
        clipped_mask = sub['log2fc'] == -np.inf

        for i, dataset_id in enumerate(dataset_order):
            color  = OUTCOME_COLORS.get(outcome_map.get(dataset_id, ''), '#888888')
            ds     = sub[sub['dataset_id'] == dataset_id]
            y_vals = ds['_y'].dropna()

            if len(y_vals) == 0:
                continue

            # Violin (only when enough points for a meaningful KDE)
            if len(y_vals) >= 5:
                parts = ax.violinplot(
                    y_vals, positions=[i], widths=0.65,
                    showmedians=True, showextrema=False,
                )
                for pc in parts['bodies']:
                    pc.set_facecolor(color)
                    pc.set_alpha(0.35)
                    pc.set_edgecolor('none')
                parts['cmedians'].set_color('#333333')
                parts['cmedians'].set_linewidth(1.5)
                parts['cmedians'].set_zorder(4)

            # Regular points (finite log2fc)
            finite = ds[~clipped_mask.reindex(ds.index, fill_value=False) & ds['_y'].notna()]
            if len(finite):
                jx = rng.uniform(-0.15, 0.15, size=len(finite))
                ax.scatter(
                    i + jx, finite['_y'],
                    color=color, s=18, alpha=0.75, linewidths=0, zorder=3,
                )

            # Clipped points (tpm_y = 0): downward triangle at floor
            clipped = ds[clipped_mask.reindex(ds.index, fill_value=False)]
            if len(clipped):
                jx = rng.uniform(-0.15, 0.15, size=len(clipped))
                ax.scatter(
                    i + jx, clipped['_y'],
                    marker='v', color=color, s=28, alpha=0.85,
                    linewidths=0.4, edgecolors='#333333', zorder=3,
                )

        # Reference lines
        ax.axhline(0,            color='#888888', lw=0.9, ls='--', zorder=0)
        ax.axhline(-1,           color='#bbbbbb', lw=0.6, ls=':',  zorder=0)
        ax.axhline(LOG2FC_FLOOR, color='#dddddd', lw=0.6, ls=':',  zorder=0)

        # Outcome group dividers
        for outcome in OUTCOME_ORDER[:-1]:
            mask = meta_sorted['outcome'] == outcome
            if mask.any():
                last_pos = mask[mask].index[-1]
                ax.axvline(last_pos + 0.5, color='#aaaaaa', lw=0.8, zorder=0)

        n_genes = sub.groupby('dataset_id')['gene_id'].nunique().median()
        ax.set_title(f'{title}  (n\u202f≈\u202f{int(n_genes)} genes per dataset)',
                     fontsize=11, pad=6)
        ax.set_ylabel('log₂FC  (expt / ref)', fontsize=10)
        ax.spines[['top', 'right']].set_visible(False)

    # x-axis labels on bottom panel only
    axes[-1].set_xticks(x_pos)
    axes[-1].set_xticklabels(dataset_order, rotation=90, fontsize=7, ha='center')

    # Shared legend
    legend_handles = [
        mpatches.Patch(facecolor=OUTCOME_COLORS[o], label=OUTCOME_LABELS[o])
        for o in OUTCOME_ORDER
    ]
    fig.legend(
        handles=legend_handles,
        loc='upper center', ncol=3, fontsize=9,
        frameon=False, bbox_to_anchor=(0.5, 1.01),
    )

    fig.savefig(out_path, dpi=150, bbox_inches='tight')
    plt.close(fig)
    print(f"Saved: {out_path}")
    return out_path


