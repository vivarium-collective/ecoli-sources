def plot_pca(
    df_all: pd.DataFrame,
    meta_df: pd.DataFrame,
    gene_cols: list = None,
    dataset_ids: list = None,
    out_path: Path = None,
) -> Path:
    """
    PCA of per-dataset log2FC profiles for a specified gene subset.

    Each dataset is a point in the score plot (PC1 × PC2), colored by the
    extended 4-level outcome (success / sim_failed / parca_fail_psolve /
    parca_fail_aa_kcat). Dataset labels are shown next to each point.

    Feature matrix: datasets × genes selected by OR-ing the boolean columns in
    gene_cols. -inf values (gene absent from experiment) are clipped to
    LOG2FC_FLOOR. Missing values (gene absent from reference) are filled with 0.

    Parameters
    ----------
    df_all : Long-format DataFrame from _load_allgenes().
    meta_df : Meta-summary DataFrame with parca_status and parca_error columns.
    gene_cols : Boolean columns in df_all whose union defines the gene feature set.
                Default: ['is_essential', 'has_expression_adjustment'].
    dataset_ids : Optional list of dataset IDs to include. Defaults to all datasets.
    out_path : Save path. Defaults to analysis/figures/meta_pca.png.

    Returns
    -------
    Path where the figure was saved.
    """
    if gene_cols is None:
        gene_cols = ['is_essential', 'has_expression_adjustment']
    if out_path is None:
        out_path = FIGURES_DIR / 'meta_pca.png'

    if dataset_ids is not None:
        meta_df = meta_df[meta_df['dataset_id'].isin(dataset_ids)].reset_index(drop=True)
        df_all  = df_all[df_all['dataset_id'].isin(dataset_ids)]

    outcome_ext = _derive_extended_outcome(meta_df)
    outcome_map = dict(zip(meta_df['dataset_id'], outcome_ext))

    # Build gene mask as union of specified boolean columns
    gene_mask = pd.Series(False, index=df_all.index)
    for col in gene_cols:
        gene_mask = gene_mask | df_all[col].fillna(False).astype(bool)
    sub = df_all[gene_mask].copy()
    sub['_y'] = sub['log2fc'].replace(-np.inf, LOG2FC_FLOOR).replace(np.inf, np.nan)

    pivot = (
        sub.groupby(['dataset_id', 'gene_symbol'])['_y']
        .first()
        .unstack('gene_symbol')
        .fillna(0)   # gene absent from reference → neutral
    )

    # PCA
    X = pivot.values
    pca = PCA(n_components=2)
    coords = pca.fit_transform(X)
    var_exp = pca.explained_variance_ratio_ * 100

    dataset_ids = pivot.index.tolist()

    fig, ax = plt.subplots(figsize=(8, 6.5))

    # Plot by outcome group so legend order is consistent
    plotted = set()
    for outcome in OUTCOME_ORDER_EXT:
        color = OUTCOME_COLORS_EXT[outcome]
        label = OUTCOME_LABELS_EXT[outcome]
        idxs  = [i for i, d in enumerate(dataset_ids)
                 if outcome_map.get(d) == outcome]
        if not idxs:
            continue
        ax.scatter(
            coords[idxs, 0], coords[idxs, 1],
            c=color, s=90, zorder=3, linewidths=0.5, edgecolors='white',
            label=label,
        )
        plotted.add(outcome)

    # Dataset labels
    for i, dataset_id in enumerate(dataset_ids):
        short = dataset_id.replace('precise_', '').replace('gbw_vegas_', '')
        ax.annotate(
            short,
            xy=(coords[i, 0], coords[i, 1]),
            xytext=(5, 3), textcoords='offset points',
            fontsize=6.5, color='#333333',
            path_effects=[pe.withStroke(linewidth=1.5, foreground='white')],
        )

    ax.axhline(0, color='#cccccc', lw=0.7, ls='--', zorder=0)
    ax.axvline(0, color='#cccccc', lw=0.7, ls='--', zorder=0)
    ax.set_xlabel(f'PC1  ({var_exp[0]:.1f}% variance explained)', fontsize=10)
    ax.set_ylabel(f'PC2  ({var_exp[1]:.1f}% variance explained)', fontsize=10)
    gene_set_label = ' + '.join(c.replace('is_', '').replace('has_', '').replace('_', ' ') for c in gene_cols)
    ax.set_title(
        f'PCA of {gene_set_label} gene log₂FC profiles\n'
        f'({X.shape[1]} genes × {X.shape[0]} datasets)',
        fontsize=11,
    )
    ax.spines[['top', 'right']].set_visible(False)
    ax.legend(fontsize=8, frameon=False, loc='best')

    fig.tight_layout()
    fig.savefig(out_path, dpi=150, bbox_inches='tight')
    plt.close(fig)
    print(f"Saved: {out_path}")
    return out_path


