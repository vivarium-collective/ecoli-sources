def plot_pearson_r2_bars(
    df: pd.DataFrame,
    out_path: Path = None,
) -> Path:
    """
    Three-panel bar plot of Pearson r² (log10 scale) comparing each dataset
    to the reference, for three gene subsets:
      - All in-model genes
      - Expression-adjusted genes (any, essential or not)
      - High-expression genes (TPM > 100 in reference)

    Datasets are ordered by outcome (success → sim_failed → parca_failed) and
    then by descending r² within each group. Each bar is colored by outcome.

    Parameters
    ----------
    df : Meta-summary DataFrame from generate_meta_summary().
    out_path : Save path. Defaults to analysis/figures/meta_pearson_r2_bars.png.

    Returns
    -------
    Path where the figure was saved.
    """
    if out_path is None:
        out_path = FIGURES_DIR / 'meta_pearson_r2_bars.png'

    df = _sort_datasets(df)

    panels = [
        ('all__pearson_r',       'all__n_corr',       'All in-model genes'),
        ('expr_adj__pearson_r',  'expr_adj__n_corr',  'Expression-adjusted genes'),
        ('tpm_gt100__pearson_r', 'tpm_gt100__n_corr', 'High-expression genes\n(TPM\u00a0>\u00a0100 in reference)'),
    ]

    n_datasets = len(df)
    x = np.arange(n_datasets)
    colors = [OUTCOME_COLORS.get(o, '#888888') for o in df['outcome']]

    fig, axes = plt.subplots(1, 3, figsize=(14, 5), sharey=False)

    for ax, (col, n_col, title) in zip(axes, panels):
        r2 = df[col].astype(float) ** 2
        bars = ax.bar(x, r2, color=colors, edgecolor='white', linewidth=0.5, width=0.75)

        n_median = int(df[n_col].median())
        ax.set_title(f'{title}\n(n\u202f=\u202f{n_median} genes)', fontsize=11, pad=8)
        ax.set_ylabel('Pearson r²', fontsize=10)
        ax.set_ylim(0, 1.05)
        ax.set_xticks(x)
        ax.set_xticklabels(df['dataset_id'], rotation=90, fontsize=7, ha='center')
        ax.axhline(1.0, color='#cccccc', lw=0.8, ls='--', zorder=0)
        ax.spines[['top', 'right']].set_visible(False)

        # Vertical dividers between outcome groups
        for outcome in OUTCOME_ORDER[:-1]:
            mask = df['outcome'] == outcome
            if mask.any():
                last_idx = mask[mask].index[-1]
                pos = df.index.get_loc(last_idx)
                ax.axvline(pos + 0.5, color='#aaaaaa', lw=0.8, ls='-', zorder=0)

    # Shared legend
    legend_handles = [
        mpatches.Patch(facecolor=OUTCOME_COLORS[o], label=OUTCOME_LABELS[o])
        for o in OUTCOME_ORDER
    ]
    fig.legend(
        handles=legend_handles,
        loc='upper center',
        ncol=3,
        fontsize=9,
        frameon=False,
        bbox_to_anchor=(0.5, 1.02),
    )

    fig.tight_layout()
    fig.savefig(out_path, dpi=150, bbox_inches='tight')
    plt.close(fig)
    print(f"Saved: {out_path}")
    return out_path


