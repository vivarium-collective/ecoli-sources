def _derive_extended_outcome(meta_df: pd.DataFrame) -> pd.Series:
    """
    Derive outcome series from parca_status and parca_error columns.

      success              — parca completed, no sim errors
      sim_failed           — parca completed, sim errors present
      parca_fail_aa_kcat   — parca failed with kcat error (generic; used for PCA)
      parca_fail_psolve    — parca failed with p-solve (promoter binding) error
    """
    outcome = meta_df['outcome'].copy()
    kcat_mask = (
        meta_df['parca_status'].eq('FAILED') &
        meta_df['parca_error'].str.contains('kcat', case=False, na=False)
    )
    psolve_mask = (
        meta_df['parca_status'].eq('FAILED') &
        meta_df['parca_error'].str.contains('Solver could not find optimal value', na=False)
    )
    outcome[kcat_mask]   = 'parca_fail_aa_kcat'
    outcome[psolve_mask] = 'parca_fail_psolve'
    return outcome


def _derive_kcat_aa(parca_error: str):
    """Extract the amino acid abbreviation from a kcat error message, e.g. 'CYS'."""
    if pd.isna(parca_error):
        return None
    m = re.search(r'kcat for (\w+)\[', str(parca_error))
    return m.group(1) if m else None


def _latest_meta_summary() -> Path:
    """Return the most recently modified meta_summary TSV in analysis/results/."""
    candidates = sorted(RESULTS_DIR.glob('*meta_summary*.tsv'), key=lambda p: p.stat().st_mtime)
    if not candidates:
        raise FileNotFoundError(f"No meta_summary TSV found in {RESULTS_DIR}")
    return candidates[-1]


def _sort_datasets(df: pd.DataFrame) -> pd.DataFrame:
    """
    Order rows by outcome (success → sim_failed → parca_failed), then by the
    metric value of the first panel descending, so bars within each group run
    high → low.
    """
    cat = pd.Categorical(df['outcome'], categories=OUTCOME_ORDER, ordered=True)
    df = df.copy()
    df['_outcome_cat'] = cat
    df = df.sort_values(['_outcome_cat', 'all__pearson_r'], ascending=[True, False])
    return df.drop(columns=['_outcome_cat']).reset_index(drop=True)


# y-axis floor for -inf log2fc values (gene absent from experiment).
# Clipped points are shown as downward triangles at this level.
LOG2FC_FLOOR = -8.0


def _load_allgenes(results_dir: Path, meta_df: pd.DataFrame) -> pd.DataFrame:
    """
    Load all concordance_allgenes TSVs and return a single long-format DataFrame
    with dataset_id and outcome columns prepended.

    Picks the most recently modified file matching each dataset_id in case of
    multiple date-prefixed versions.
    """
    frames = []
    for _, row in meta_df.iterrows():
        pattern = f'*concordance_allgenes__*__{row["dataset_id"]}.tsv'
        matches = sorted(
            results_dir.glob(pattern), key=lambda p: p.stat().st_mtime
        )
        if not matches:
            print(f"  Warning: no allgenes TSV found for {row['dataset_id']}")
            continue
        df = pd.read_csv(matches[-1], sep='\t')
        df.insert(0, 'outcome',    row['outcome'])
        df.insert(0, 'dataset_id', row['dataset_id'])
        frames.append(df)
    return pd.concat(frames, ignore_index=True)


