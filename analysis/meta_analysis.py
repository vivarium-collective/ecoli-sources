# Figure 3: log2FC heatmap for a specific gene subset
# ---------------------------------------------------------------------------

def plot_log2fc_heatmap(
    df_all: pd.DataFrame,
    meta_df: pd.DataFrame,
    flag_col: str = 'has_expression_adjustment',
    title: str = 'Expression-adjusted genes — log₂FC (expt / ref)',
    vmax: float = 4.0,
    out_path: Path = None,
) -> Path:
    """
    Heatmap of log2FC per gene × dataset for a gene subset defined by flag_col.

    Rows are genes sorted by median log2FC ascending (most consistently
    downregulated at top). Columns are datasets in the standard outcome order
    (success → sim_failed → parca_failed), then descending Pearson r² within
    each group. Column labels are colored by outcome.

    -inf values (gene absent from experiment) are clipped to LOG2FC_FLOOR and
    annotated with a downward triangle. +inf values (gene absent from reference)
    are shown in grey.

    Parameters
    ----------
    df_all : Long-format DataFrame from _load_allgenes().
    meta_df : Meta-summary DataFrame (used for dataset ordering and outcome).
    flag_col : Boolean column in df_all used to select the gene subset.
               Default: 'has_expression_adjustment'.
    title : Figure title.
    vmax : Colormap symmetric limit (vmin = -vmax). Values outside are clipped
           to the colormap extremes. Default: 4.0.
    out_path : Save path. Defaults to analysis/figures/meta_heatmap_{flag_col}.png.

    Returns
    -------
    Path where the figure was saved.
    """
    if out_path is None:
        out_path = FIGURES_DIR / f'meta_heatmap_{flag_col}.png'

    # --- Build pivot table: rows=genes, cols=datasets ---
    meta_sorted  = _sort_datasets(meta_df)
    dataset_order = meta_sorted['dataset_id'].tolist()
    outcome_map  = meta_sorted.set_index('dataset_id')['outcome'].to_dict()

    sub = df_all[df_all[flag_col].fillna(False).astype(bool)].copy()
    sub['_y'] = sub['log2fc'].replace(-np.inf, LOG2FC_FLOOR).replace(np.inf, np.nan)

    pivot = (
        sub.groupby(['gene_symbol', 'dataset_id'])['_y']
        .first()
        .unstack('dataset_id')
        .reindex(columns=dataset_order)
    )

    # Sort genes: most consistently downregulated (lowest median) at top
    pivot = pivot.loc[pivot.median(axis=1).sort_values().index]

    gene_labels    = pivot.index.tolist()
    n_genes        = len(gene_labels)
    n_ds           = len(dataset_order)
    mat            = pivot.values.astype(float)   # NaN where gene absent from ref

    # --- Plot ---
    cell_w, cell_h = 0.50, 0.44
    fig_w = max(9, n_ds * cell_w + 2.5)
    fig_h = max(3, n_genes * cell_h + 1.5)
    fig, ax = plt.subplots(figsize=(fig_w, fig_h))

    cmap = plt.get_cmap('RdBu').copy()
    cmap.set_bad('#dddddd')   # NaN (gene absent from reference) → grey

    im = ax.imshow(
        mat, aspect='auto', cmap=cmap,
        vmin=-vmax, vmax=vmax,
        interpolation='nearest',
    )

    # Cell annotations
    for row in range(n_genes):
        for col in range(n_ds):
            val = mat[row, col]
            if np.isnan(val):
                continue
            is_clipped = (sub.loc[
                (sub['gene_symbol'] == gene_labels[row]) &
                (sub['dataset_id']  == dataset_order[col]),
                'log2fc'
            ] == -np.inf).any()
            txt = f'{val:.1f}'
            color = 'white' if abs(val) > vmax * 0.6 else '#333333'
            ax.text(col, row, txt, ha='center', va='center',
                    fontsize=6.5, color=color)
            if is_clipped:
                ax.text(col + 0.3, row - 0.3, '▼', ha='center', va='center',
                        fontsize=5, color=color)

    # Axes
    ax.set_xticks(range(n_ds))
    ax.set_xticklabels(dataset_order, rotation=90, fontsize=7.5, ha='center')
    ax.set_yticks(range(n_genes))
    ax.set_yticklabels(gene_labels, fontsize=9)
    ax.tick_params(length=0)

    # Color x-tick labels by outcome
    for tick, dataset_id in zip(ax.get_xticklabels(), dataset_order):
        tick.set_color(OUTCOME_COLORS.get(outcome_map.get(dataset_id, ''), '#333333'))

    # Vertical dividers between outcome groups
    for outcome in OUTCOME_ORDER[:-1]:
        mask = meta_sorted['outcome'] == outcome
        if mask.any():
            last_pos = mask[mask].index[-1]
            ax.axvline(last_pos + 0.5, color='white', lw=2, zorder=5)

    ax.set_title(title, fontsize=12, pad=10)

    # Colorbar
    cbar = fig.colorbar(im, ax=ax, fraction=0.025, pad=0.02)
    cbar.set_label('log₂FC', fontsize=9)
    cbar.ax.tick_params(labelsize=8)

    fig.tight_layout()

    # Outcome legend centered below x-axis labels
    legend_handles = [
        mpatches.Patch(facecolor=OUTCOME_COLORS[o], label=OUTCOME_LABELS[o])
        for o in OUTCOME_ORDER
    ]
    fig.legend(
        handles=legend_handles,
        fontsize=8, frameon=False,
        loc='lower center', bbox_to_anchor=(0.5, -0.04),
        ncol=3,
    )

    fig.savefig(out_path, dpi=150, bbox_inches='tight')
    plt.close(fig)
    print(f"Saved: {out_path}")
    return out_path


