# Figure 5: AA enzyme downregulation frequency by outcome
# ---------------------------------------------------------------------------

def plot_aa_downreg_frequency(
    df_all: pd.DataFrame,
    meta_df: pd.DataFrame,
    threshold: float = -1.0,
    out_path: Path = None,
) -> Path:
    """
    Heatmap of downregulation frequency for AA pathway enzyme genes.

    Columns (left → right):
      success (n=?) | parca_fail_psolve (n=?) | parca_fail_CYS | parca_fail_TRP | parca_fail_ILE
    sim_failed is excluded as it reflects a Ginkgo batch effect rather than a
    distinct mechanistic failure mode.

    Each cell shows the fraction of datasets in that column group where the
    gene's log2fc < threshold. For the single-dataset kcat columns (n=1), the
    value is 0% or 100%.

    Genes are sorted by (max kcat-column frequency − success frequency) so the
    most discriminating genes rise to the top. A colored strip on the left
    indicates each gene's AA pathway assignment.

    Parameters
    ----------
    df_all : Long-format DataFrame from _load_allgenes().
    meta_df : Meta-summary DataFrame with parca_status and parca_error columns.
    threshold : log2fc cutoff for flagging downregulation. Default -1.0.
    out_path : Save path. Defaults to analysis/figures/meta_aa_downreg_freq.png.

    Returns
    -------
    Path where the figure was saved.
    """
    if out_path is None:
        out_path = FIGURES_DIR / 'meta_aa_downreg_freq.png'

    # Build per-dataset outcome with kcat split by AA
    meta = meta_df.copy()
    meta['_kcat_aa'] = meta['parca_error'].apply(_derive_kcat_aa)

    def _full_outcome(row):
        if row['outcome'] == 'parca_failed':
            if pd.notna(row.get('_kcat_aa')):
                return f'parca_fail_{row["_kcat_aa"]}'
            return 'parca_fail_psolve'
        return row['outcome']

    meta['_outcome_full'] = meta.apply(_full_outcome, axis=1)
    ds_outcome = dict(zip(meta['dataset_id'], meta['_outcome_full']))

    # Identify which kcat AAs are actually present in this dataset collection
    kcat_aas = sorted({v.replace('parca_fail_', '') for v in ds_outcome.values()
                       if v.startswith('parca_fail_') and v != 'parca_fail_psolve'})
    kcat_cols = [f'parca_fail_{aa}' for aa in kcat_aas]

    col_order = ['success', 'parca_fail_psolve'] + kcat_cols
    n_per_col = {c: sum(1 for v in ds_outcome.values() if v == c) for c in col_order}

    col_labels = []
    col_colors = []
    for c in col_order:
        n = n_per_col[c]
        if c == 'success':
            label, color = f'Success\n(n={n})', OUTCOME_COLORS_EXT['success']
        elif c == 'parca_fail_psolve':
            label, color = f'ParCa failed\np-solve (n={n})', OUTCOME_COLORS_EXT['parca_fail_psolve']
        else:
            aa = c.replace('parca_fail_', '')
            label, color = f'ParCa failed\nkcat {aa} (n={n})', OUTCOME_COLORS_EXT['parca_fail_aa_kcat']
        col_labels.append(label)
        col_colors.append(color)

    # Gene × outcome frequency matrix
    aa = df_all[df_all['aa_pway_enzyme'].fillna(False).astype(bool)].copy()
    aa['_y']       = aa['log2fc'].replace(-np.inf, LOG2FC_FLOOR).replace(np.inf, np.nan)
    aa['_outcome'] = aa['dataset_id'].map(ds_outcome)
    aa['_flag']    = aa['_y'] < threshold

    freq = (
        aa.groupby(['gene_symbol', '_outcome'])['_flag']
        .mean()
        .unstack('_outcome')
        .reindex(columns=col_order)
        .fillna(0)
    )

    # Filter: at least one dataset below threshold
    freq = freq[freq.max(axis=1) > 0]

    # Sort: pathway first (known failure pathways CYS/TRP/ILE lead), then by
    # discrimination score descending within each pathway
    pathway_priority = ['CYS', 'CYS/TRP', 'TRP', 'ILE', 'ILE/VAL', 'MET',
                        'LEU', 'VAL', 'ALA', 'ALA/VAL', 'THR', 'THR/GLY',
                        'SER', 'GLY/SER', 'ARG', 'ASN', 'ASP', 'GLN', 'GLU',
                        'HIS', 'LYS', 'PHE', 'TYR', 'PRO', 'PYR', 'PUR', 'other']
    pathway_rank = {p: i for i, p in enumerate(pathway_priority)}

    disc = freq[kcat_cols].max(axis=1) - freq['success']
    freq['_pwy']  = [AA_PATHWAY_MAP.get(g, 'other') for g in freq.index]
    freq['_prank'] = freq['_pwy'].map(pathway_rank).fillna(99)
    freq['_disc']  = disc
    freq = freq.sort_values(['_prank', '_disc'], ascending=[True, False])
    freq = freq.drop(columns=['_pwy', '_prank', '_disc'])

    gene_labels = freq.index.tolist()
    pathways    = [AA_PATHWAY_MAP.get(g, 'other') for g in gene_labels]
    n_genes     = len(gene_labels)
    n_cols      = len(col_order)

    # Layout: narrow pathway strip | main heatmap
    cell_h  = 0.38
    fig_w   = n_cols * 0.95 + 3.5
    fig_h   = max(4, n_genes * cell_h + 1.8)
    fig, (ax_pw, ax) = plt.subplots(
        1, 2, figsize=(fig_w, fig_h),
        gridspec_kw={'width_ratios': [0.50, n_cols], 'wspace': 0.25},
    )

    # --- Pathway strip ---
    for i, (gene, pwy) in enumerate(zip(gene_labels, pathways)):
        color = AA_PATHWAY_COLORS.get(pwy, '#cccccc')
        ax_pw.barh(i, 1, color=color, edgecolor='white', linewidth=0.3)
        ax_pw.text(0.5, i, pwy, ha='center', va='center', fontsize=6.5,
                   color='white' if pwy not in ('other', 'PUR') else '#555555',
                   fontweight='bold')
    ax_pw.set_xlim(0, 1)
    ax_pw.set_ylim(-0.5, n_genes - 0.5)
    ax_pw.invert_yaxis()
    ax_pw.axis('off')
    ax_pw.set_title('Pathway', fontsize=8, pad=6)

    # --- Main heatmap ---
    mat  = freq.values
    cmap = plt.get_cmap('Oranges')
    im   = ax.imshow(mat, aspect='auto', cmap=cmap, vmin=0, vmax=1,
                     interpolation='nearest')

    for row in range(n_genes):
        for col in range(n_cols):
            val = mat[row, col]
            txt   = f'{val:.0%}' if val > 0 else '—'
            color = 'white' if val > 0.6 else '#333333'
            ax.text(col, row, txt, ha='center', va='center',
                    fontsize=7.5, color=color)

    # Divider between success/psolve and kcat columns
    ax.axvline(1.5, color='white', lw=2, zorder=5)

    ax.set_xticks(range(n_cols))
    ax.set_xticklabels(col_labels, fontsize=8)
    for tick, color in zip(ax.get_xticklabels(), col_colors):
        tick.set_color(color)
    ax.set_yticks(range(n_genes))
    ax.set_yticklabels(gene_labels, fontsize=8)
    ax.tick_params(length=0)
    ax.xaxis.tick_top()
    ax.xaxis.set_label_position('top')

    ax.set_title(
        f'Fraction of datasets with log₂FC\u202f<\u202f{threshold}',
        fontsize=10, pad=32,
    )

    cbar = fig.colorbar(im, ax=ax, fraction=0.025, pad=0.02)
    cbar.set_label('Fraction', fontsize=8)
    cbar.ax.tick_params(labelsize=7)

    fig.savefig(out_path, dpi=150, bbox_inches='tight')
    plt.close(fig)
    print(f"Saved: {out_path}")
    return out_path


